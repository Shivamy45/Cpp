/*
STL  - Standard Template Library

Components of STL
1. Containers - object which stores data
    -> Stores data 
    -> Use template classes
2. Algorithms - procedure to process data
    -> Sorting
    -> Searching
    -> Use template functions
3. Iterators - object which points to an element of a container
    -> Object points to an element in a container
    -> Handled just like pointers
    -> Connects Algorithms with containers
*/